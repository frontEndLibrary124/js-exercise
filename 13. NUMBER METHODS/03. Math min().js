// finds the minimum value among the specified values and returns it

// Parameter: takes in a random number of parameters
// Returns: the smallest value / NaN

let min1 = Math.min(1, 2, 3, 4, 5);
console.log(min1);  // 1

let min2 = Math.min(-1, -2, -3, -4, -5);
console.log(min2); // -5

let min3 = Math.min('hello', 1, 2);
console.log(min3); // NaN