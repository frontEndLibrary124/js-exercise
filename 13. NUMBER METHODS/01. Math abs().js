// finds the absolute value of the specified number (without any sign) 
// and returns it

// Parameter: number
// Returns: absolute value / NaN

let val1 = Math.abs(10);
console.log(val1);  // 10

let val2 = Math.abs(-20);
console.log(val2);  // 20

let val3 = Math.abs('25');
console.log(val3);  // 25

let val4 = Math.abs('hello');
console.log(val4);  // NaN