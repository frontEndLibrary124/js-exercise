// returns the number rounded to the nearest integer

let num1 = Math.round(4.8);
console.log(num1);  // 5

let num2 = Math.round(5.2);
console.log(num2);  // 5

let num3 = Math.round(4.5);
console.log(num3);  // 5

let num4 = Math.round(-5.5);
console.log(num4);  // -5

let num5 = Math.round(null);
console.log(num5); // 0

let num6 = Math.round('hello');
console.log(num6); // NaN