// computes the power of a number by raising the second argument 
// to the power of the first argument

let power1 = Math.pow(5, 3);
console.log(power1); // 125

let power2 = Math.pow(-5, -3);
console.log(power2);  // -0.008

let power3 = Math.pow(0, -5);
console.log(power3);  // Infinity

let power4 = Math.pow(4.5, 2.3);
console.log(power4);  // 31.7971929089206

let power5 = Math.pow(-5, 2.4);
console.log(power5);  // NaN