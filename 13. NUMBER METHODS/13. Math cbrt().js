// computes the cube root of a specified number and returns it

let num1 = Math.cbrt(8);
console.log(num1);  // 2

let num2 = Math.cbrt('64');
console.log(num2);  // 4

let num3 = Math.cbrt(32.465);
console.log(num3);  // 3.1901061602560543

let num4 = Math.cbrt('hello');
console.log(num4);  // NaN