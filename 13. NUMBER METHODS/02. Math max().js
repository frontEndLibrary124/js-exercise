// finds the maximum value among the specified values and returns it

// Parameter: takes in a random number of parameters
// Returns: the largest value / NaN

let max1 = Math.max(1, 2, 3, 4, 5);
console.log(max1);  // 5

let max2 = Math.max(-1, -2, -3, -4, -5);
console.log(max2); // -1

let max3 = Math.max('hello', 1, 2);
console.log(max3); // NaN