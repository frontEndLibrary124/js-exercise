// returns e (Euler's number) raised to given power
// It is equivalent to e^x in mathematics

// Parameter: x -> a number
// Returns: e^x for argument x, where e is Euler's number (2.71828)

let val1 = Math.exp(0);
console.log(val1); // 1

let val2 = Math.exp(1);
console.log(val2); // 2.718281828459045

let val3 = Math.exp("-1");
console.log(val3); // 0.36787944117144233

let val4 = Math.exp(2);
console.log(val4); // 7.38905609893065

let val5 = Math.exp(5);
console.log(val5); // 148.4131591025766